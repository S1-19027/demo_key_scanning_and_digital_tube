#include "stm32f10x.h"                  // Device header
void Delay_us(uint32_t xus)
{
	SysTick->LOAD = 72 * xus;				//????????
	SysTick->VAL = 0x00;					  //???????
	SysTick->CTRL = 0x00000005;			//??????HCLK,?????
	while(!(SysTick->CTRL & 0x00010000));	//?????0
	SysTick->CTRL = 0x00000004;			//?????
}
